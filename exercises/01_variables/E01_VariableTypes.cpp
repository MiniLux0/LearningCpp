/*
 * E01 — Variable Types
 * --------------------
 * Declara una variable de cada tipo: int, double, char, bool.
 * Asignale un valor e imprimela con cout.
 *
 * Salida esperada:
 *   int: 42
 *   double: 3.14
 *   char: A
 *   bool: 1
 */

#include <iostream>
using namespace std;

int main() {

    int iValue = 42;
    double dValue = 3.14;
    char cValue = 'B';
    bool bValue = true;

    cout << iValue << "\n";
    cout << dValue << "\n";
    cout << cValue << "\n";
    cout << bValue << "\n";
    

    return 0;
}
