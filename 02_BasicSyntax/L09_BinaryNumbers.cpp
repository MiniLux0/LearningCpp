// L09 - Binary Numbers and Computer Memory
//
// DECIMAL SYSTEM (base 10):
// 372 = 2x1 + 7x10 + 3x100
//
// BINARY SYSTEM (base 2):
// Only two digits: 0 and 1
// 1011 = 1x1 + 1x2 + 0x4 + 1x8 = 1+2+0+8 = 11 in decimal
//
// BITS AND BYTES:
// - Bit: a single memory cell, stores 0 or 1 (off/on)
// - Byte: 8 bits grouped together
//
// WHY IT MATTERS:
// - Every variable type occupies a certain number of bytes
// - More bytes = larger range of values
// - int = 4 bytes = 32 bits
// - char = 1 byte = 8 bits
// - long long = 8 bytes = 64 bits
//
// Each type has a minimum and maximum value it can store.
// Exceeding that range causes OVERFLOW.